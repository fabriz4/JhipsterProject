/* tslint:disable max-line-length */
import { ComponentFixture, TestBed } from '@angular/core/testing';
import { Observable, of } from 'rxjs';
import { HttpHeaders, HttpResponse } from '@angular/common/http';
import { ActivatedRoute, Data } from '@angular/router';

import { TvBookMarksTestModule } from '../../../test.module';
import { Serie_tv_preferiteComponent } from 'app/entities/serie-tv-preferite/serie-tv-preferite.component';
import { Serie_tv_preferiteService } from 'app/entities/serie-tv-preferite/serie-tv-preferite.service';
import { Serie_tv_preferite } from 'app/shared/model/serie-tv-preferite.model';

describe('Component Tests', () => {
  describe('Serie_tv_preferite Management Component', () => {
    let comp: Serie_tv_preferiteComponent;
    let fixture: ComponentFixture<Serie_tv_preferiteComponent>;
    let service: Serie_tv_preferiteService;

    beforeEach(() => {
      TestBed.configureTestingModule({
        imports: [TvBookMarksTestModule],
        declarations: [Serie_tv_preferiteComponent],
        providers: [
          {
            provide: ActivatedRoute,
            useValue: {
              data: {
                subscribe: (fn: (value: Data) => void) =>
                  fn({
                    pagingParams: {
                      predicate: 'id',
                      reverse: false,
                      page: 0
                    }
                  })
              }
            }
          }
        ]
      })
        .overrideTemplate(Serie_tv_preferiteComponent, '')
        .compileComponents();

      fixture = TestBed.createComponent(Serie_tv_preferiteComponent);
      comp = fixture.componentInstance;
      service = fixture.debugElement.injector.get(Serie_tv_preferiteService);
    });

    it('Should call load all on init', () => {
      // GIVEN
      const headers = new HttpHeaders().append('link', 'link;link');
      spyOn(service, 'query').and.returnValue(
        of(
          new HttpResponse({
            body: [new Serie_tv_preferite(123)],
            headers
          })
        )
      );

      // WHEN
      comp.ngOnInit();

      // THEN
      expect(service.query).toHaveBeenCalled();
      expect(comp.serie_tv_preferites[0]).toEqual(jasmine.objectContaining({ id: 123 }));
    });

    it('should load a page', () => {
      // GIVEN
      const headers = new HttpHeaders().append('link', 'link;link');
      spyOn(service, 'query').and.returnValue(
        of(
          new HttpResponse({
            body: [new Serie_tv_preferite(123)],
            headers
          })
        )
      );

      // WHEN
      comp.loadPage(1);

      // THEN
      expect(service.query).toHaveBeenCalled();
      expect(comp.serie_tv_preferites[0]).toEqual(jasmine.objectContaining({ id: 123 }));
    });

    it('should re-initialize the page', () => {
      // GIVEN
      const headers = new HttpHeaders().append('link', 'link;link');
      spyOn(service, 'query').and.returnValue(
        of(
          new HttpResponse({
            body: [new Serie_tv_preferite(123)],
            headers
          })
        )
      );

      // WHEN
      comp.loadPage(1);
      comp.reset();

      // THEN
      expect(comp.page).toEqual(0);
      expect(service.query).toHaveBeenCalledTimes(2);
      expect(comp.serie_tv_preferites[0]).toEqual(jasmine.objectContaining({ id: 123 }));
    });
    it('should calculate the sort attribute for an id', () => {
      // WHEN
      const result = comp.sort();

      // THEN
      expect(result).toEqual(['id,asc']);
    });

    it('should calculate the sort attribute for a non-id attribute', () => {
      // GIVEN
      comp.predicate = 'name';

      // WHEN
      const result = comp.sort();

      // THEN
      expect(result).toEqual(['name,asc', 'id']);
    });
  });
});
